package com.company;

public class Main {

    public static void main(String[] args) {
        // This program will convert pounds to kilograms.
        double weightInPounds = 200D;
        double weightInKilograms = weightInPounds * 0.45359237D;
        System.out.println(weightInPounds + " pounds converted into kilograms is " + weightInKilograms + " KG.");
    }
}
